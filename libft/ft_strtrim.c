#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*result;
	int		end;

	if (!s1 || !set)
		return (NULL);
	while (*s1 && ft_strchr(set, *s1))
		s1++;
	end = ft_strlen(s1);
	while (end > 0 && ft_strchr(set, s1[end - 1]))
		end--;
	result = ft_substr(s1, 0, end);
	return (result);
}
